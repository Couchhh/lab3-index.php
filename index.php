            <h1>The Big Deal About <strong>Fishing</strong></h1>
            <section>
                <h2>what is industrial-scale fishing</h2>
                <p>Fishing is negatively impacting our environment, and I'm not talking about the kind where you go out with your pals to see who can catch the biggest fish. I'm talking about real big boats dragging thousands of fish in massive nets. These boats in particular are decreasing the population of dolphin, sea turtle, and shark population due to accidentally catching them up. On top of this, they are secreting loads of CO2 into the atmosphere from the amount of fuel they eat up from these trips. </p>
            </section>
            <section>
                <h2>impact in the ocean</h2>
                <p>According to National Geographic, more than 55 percent of the worlds ocean has been fished upon by these large scale boats. The term bottom trawling refers to the practice of dragging big nets across the ocean floor, and sometimes they catch some unexpected friends. These products are what they call bycatch, which are mainly turtles, dolphins, and sharks. All of these marine life are endangered with one of the factors being fishing. On top of this, sharks are apex predators, which means the absence of them can mean a disruption in the food chain. If bottom trawling continues, it could lead to catastrophic changes with the marine food chain.</p>
            </section>
