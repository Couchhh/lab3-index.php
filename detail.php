            <h2> Description</h2>
            <p> Nurse sharks are common sea dwellers in the shallower parts of the west Atlantic and eastern Pacific oceans. They are light brown, and goldish colors. Something quite interesting about this species of shark, is that they are smooth to the touch and harmless to humans. It is true that they have sharp teeth and can get up to 14 feet long, however, they steer clear from humans.</p>
